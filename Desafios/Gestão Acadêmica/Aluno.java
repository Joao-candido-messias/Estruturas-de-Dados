public static class Aluno{
    public String nome;
    public String curso;
    public String sexo; 
    public int anoIngresso;

    public Aluno(String nome, String curso, String sexo, int anoIngresso){
        this.nome = nome;
        this.curso = curso;
        this.sexo = sexo;
        this.anoIngresso = anoIngresso;
    }
}